public class Task {

    private int time;
    private int value;

    public Task(int t, int v){
        this.time=t;
        this.value=v;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    
    
}
